function first(number1, number2) {
	var a = number1 + number2;
	return a;
}
document.write("10 + 12 =" +first(10, 12) +"<br/>");

function second(number1, number2) {
	var b = number1 - number2;
	return b;
}
document.write("58 - 24 =" +second(58, 24) +"<br/>");

function third(number1, number2) {
	var c = number1 * number2;
	return c;
}
document.write("6 x 7 =" +third(6, 7) +"<br/>");


function fourth(number1, number2) {
	var d = number1 / number2;
	return d;
}
document.write("144 : 12 =" +fourth(144, 12) +"<br/>");


function fifth(number1) {
	var e = number1 + 1;
	return e;
}
document.write("12 + 1 =" +fifth(12) +"<br/>");

function sixth(number1) {
	var f = number1 - 1;
	return f;
}
document.write("34 - 1 =" +sixth(34) +"<br/>");
